function sortNumbers(digit) {
    let i = 0
    switch (digit) {
        case 'one':
            i = 1
            break
        case 'two':
            i = 2
            break
        case 'three':
            i = 3
            break
        case 'four':
            i = 4
            break
        case 'five':
            i = 5
            break
        case 'six':
            i = 6
            break
        case 'seven':
            i = 7
            break
        case 'eight':
            i = 8
            break
        case 'nine':
            i = 9
            break
        case 'zero':
            i = 0
            break
    }
    console.log(i)
}
sortNumbers('zero')